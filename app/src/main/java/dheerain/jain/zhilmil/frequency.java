public class frequency {
    int freq;
    int milisec;

    public frequency(int freq, int milisec) {
        this.freq = freq;
        this.milisec = milisec;
    }

    public int getFreq() {
        return freq;
    }

    public int getMilisec() {
        return milisec;
    }
}
